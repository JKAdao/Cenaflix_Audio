    /**
     * Método para limpar os campos
     *
     */
    private void limparCampos() {
        txtNome.setText("");
        txtLogin.setText("");
        txtSenha.setText("");
        boxTipo.setSelectedIndex(0);
        txtNome.requestFocus();

    }

    /**
     * Método para verificar se os campos não estão vazios
     *
     * @return
     */
    private boolean emptyFields() {

        boolean vazio = true;

        if (txtNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informe um nome para se registrar.");
            txtNome.requestFocus();

        } else if (txtLogin.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informe um login para se registrar.");
            txtLogin.requestFocus();

        } else if (txtSenha.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informe uma senha para se registrar.");
            txtSenha.requestFocus();
        } else {
            vazio = false;
        }
        return vazio;
    }

    /**
     * Método para validar a senha, que deve conter caracteres numéricos Letras
     * maiúsculas e minúsculas, e caracteres especiais
     *
     * @param senha
     * @return Uma senha válida
     */
    public static boolean validaSenha(String senha) {
        if (senha.length() < 6) {
            return false;
        }
        boolean achouNumero = false;
        boolean achouMaiuscula = false;
        boolean achouMinuscula = false;
        boolean achouSimbolo = false;
        for (char c : senha.toCharArray()) {
            if (c >= '0' && c <= '9') {
                achouNumero = true;
            } else if (c >= 'A' && c <= 'Z') {
                achouMaiuscula = true;
            } else if (c >= 'a' && c <= 'z') {
                achouMinuscula = true;
            } else {
                achouSimbolo = true;
            }
        }
        boolean senhaValida = achouNumero && achouMaiuscula && achouMinuscula && achouSimbolo;

        return senhaValida;
    }
