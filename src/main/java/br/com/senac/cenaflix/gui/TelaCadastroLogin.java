    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String senha = txtSenha.getText();
        if (!emptyFields()) {
            Usuario novoUsuario = new Usuario();
            UsuarioDAO usuarioDao = new UsuarioDAO();

            try {
                if (validaSenha(senha) == true) {
                    novoUsuario.setNome(txtNome.getText());
                    novoUsuario.setLogin(txtLogin.getText());
                    novoUsuario.setSenha(senha);
                    novoUsuario.setTipo(boxTipo.getSelectedItem().toString());

                    usuarioDao.registrarUsuario(novoUsuario);
                    limparCampos();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "A senha deve conter ao menos um número, uma letra maiúscula e uma minúscula, e um caractere especial");
                    txtSenha.requestFocus();
                }
            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
            }

        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

