    /**
     * Método para limpar os campos
     *
     */
    public void limparCampos() {
        txtProdutor.setText("");
        txtNome.setText("");
        txtNumero.setText("");
        txtDuracao.setText("");
        txtUrl.setText("");

        txtProdutor.requestFocus();
    }

    /**
     * Método para verificar se os campos não estão vazios
     *
     * @return
     */
    private boolean emptyFields() {

        boolean vazio = true;

        if (txtProdutor.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informe um Produtor para registrar.");
            txtProdutor.requestFocus();

        } else if (txtNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informe o nome do episódio.");
            txtNome.requestFocus();

        } else if (txtNumero.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informe o número do episódio");
            txtNumero.requestFocus();

        } else if (txtDuracao.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informe a duração do episódio");
            txtDuracao.requestFocus();

        } else if (txtUrl.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informe a url do episódio");
            txtUrl.requestFocus();
        } else {
            vazio = false;
        }
        return vazio;
    }

    /**
     * Método para validar o campo de duração, devendo ser no formato hh:mm
     *
     * @return Campo de duração validado
     */
    private boolean validarCampo() {
        boolean validado = true;

        boolean tempoValida = txtDuracao.getText().matches("[0-9]{2}[:][0-9]{2}");

        if (tempoValida == false) {
            JOptionPane.showMessageDialog(rootPane, "Utilize o formato hh:mm para cadastrar a hora");
            txtDuracao.requestFocus();

        } else {
            JOptionPane.showMessageDialog(rootPane, "Dados incluidos com sucesso");
            validado = false;
        }

        return validado;
    }

