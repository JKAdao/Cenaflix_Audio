    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Podcast novoPodcast = new Podcast();
        PodcastDAO podcastDao = new PodcastDAO();

        SimpleDateFormat dateFormat = new SimpleDateFormat("hh:mm");
        if (!emptyFields()) {
            if (!validarCampo()) {
                try {
                    Date parseDate = dateFormat.parse(txtDuracao.getText());
                    Timestamp timestamp = new Timestamp(parseDate.getTime());

                    novoPodcast.setProdutor(txtProdutor.getText());
                    novoPodcast.setNomeep(txtNome.getText());
                    novoPodcast.setNumeroep(Integer.parseInt(txtNumero.getText()));
                    novoPodcast.setDuracao(timestamp);
                    novoPodcast.setUrl(txtUrl.getText());

                    podcastDao.cadastrarPodcast(novoPodcast);
                    limparCampos();

                } catch (NumberFormatException | ParseException e) {
                    JOptionPane.showMessageDialog(this, "Ocorreu uma falha ao cadastrar ou na conversão pra TimeStamp:\n" + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

